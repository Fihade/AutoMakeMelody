	// area第几组，note是音符：1 2 3 4 5 6 7 / do re mi fa so la si，过滤掉了半音
	public static byte key(byte area, byte note) {
		byte result = CENTER - 1;
		result += 12 * area;
		for (byte i = 0; i < note; i++) {
			switch (i % 7 + 1) {
			case 1:
			case 4:
				result++;
				break;
			default:
				result += 2;
			}
		}
		return result;
	}
